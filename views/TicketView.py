class TicketView:
    """
    The TicketView presents the data to the user

    """

    def display_hello_message(self):
        """
        Displays a welcome message at the start

        """

        print("hello_message")

    def display_menu(self):
        """
        Displays the menu of available options

        """

        print("menu")

    def display_goodbye_message(self):
        """
        Displays a thank you message

        """

        print("goodbye")

    def display_all_tickets(self):
        """
        Displays all the tickets

        """

        print("all tickets")

    def display_ticket_detail(self):
        """
        Displays the details of a single ticket

        """

        print("ticket detail")

    def display_error_message(self):
        """
        Displays error messages

        """

        print("error")
